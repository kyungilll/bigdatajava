package 반복문;

public class WhileTest1 {

	public static void main(String[] args) {
		
	// 반복시 사용하는 문법 while, for
	//조건이  true일 동안 {}를 반복실행. 조건은  항상 boolean
	
	int x=100;  // 초기값
	int y=200;
	// int x > 공간을 만드는 것 / 선언
	// RAM 에 공간을 만들고 두 값을 집어넣음
	// 변수가 무엇인가 > 데이터를 저장할 공간 ★
	
	
		
	while(x<y) { // 조건식
		System.out.println("가나다라");
		
		x=x+10; //증감식
		//끝날 조건을 만들어 줌 ★
		// x+10; >  CPU 가 하는 연산
		
		
		//  while문의 기본적인 형태
		
	}
	
	}

}
